-- 기존 테이블 삭제 (의존성 역순)
DROP TABLE IF EXISTS USER_INFO;
DROP TABLE IF EXISTS AVERAGE_DATA_F;
DROP TABLE IF EXISTS EDU_DATA_F_2;

-- ============================================================
-- 1. EDU_DATA_F_2: 카드 거래 데이터 (원본)
-- ============================================================
CREATE TABLE EDU_DATA_F_2 (
    BAS_YH             CHAR(6),
    SEQ                CHAR(20),
    AGE                CHAR(2),
    SEX_CD             CHAR(2),
    MBR_RK             CHAR(2),
    ATT_YM             CHAR(6),
    HOUS_SIDO_NM       CHAR(40),
    DIGT_CHNL_REG_YN   VARCHAR(2),
    DIGT_CHNL_USE_YN   VARCHAR(2),
    LIFE_STAGE         CHAR(40),

    TOT_USE_AM         DECIMAL(18,0),
    CRDSL_USE_AM       DECIMAL(18,0),
    CNF_USE_AM         DECIMAL(18,0),

    INTERIOR_AM        DECIMAL(18,0),
    INSUHOS_AM         DECIMAL(18,0),
    OFFEDU_AM          DECIMAL(18,0),
    TRVLEC_AM          DECIMAL(18,0),
    FSBZ_AM            DECIMAL(18,0),
    SVCARC_AM          DECIMAL(18,0),
    DIST_AM            DECIMAL(18,0),
    PLSANIT_AM         DECIMAL(18,0),
    CLOTHGDS_AM        DECIMAL(18,0),
    AUTO_AM            DECIMAL(18,0),

    PRIMARY KEY (BAS_YH, SEQ)
);

-- SEQ 단독 조회 최적화를 위한 인덱스 (PK는 BAS_YH, SEQ 순서라 SEQ 단독 검색 시 사용 불가)
CREATE INDEX idx_edu_seq ON EDU_DATA_F_2 (SEQ);

-- ============================================================
-- 2. USER_INFO: 고객 테이블
-- ============================================================
CREATE TABLE USER_INFO (
    SEQ      VARCHAR(20)  NOT NULL COMMENT '고객번호',
    PASSWORD VARCHAR(255) NOT NULL COMMENT '로그인 패스워드',
    SEX_CD   CHAR(2)               COMMENT '성별',
    AGE      CHAR(2)               COMMENT '연령대',
    PRIMARY KEY (SEQ)
);

-- ============================================================
-- 3. AVERAGE_DATA_F: 연령대/성별별 평균 소비
-- ============================================================
CREATE TABLE AVERAGE_DATA_F (
    SEX_CD             CHAR(2),
    AGE                CHAR(2),

    INTERIOR_AM_MEAN   DECIMAL(18,0),
    INSUHOS_AM_MEAN    DECIMAL(18,0),
    OFFEDU_AM_MEAN     DECIMAL(18,0),
    TRVLEC_AM_MEAN     DECIMAL(18,0),
    FSBZ_AM_MEAN       DECIMAL(18,0),
    SVCARC_AM_MEAN     DECIMAL(18,0),
    DIST_AM_MEAN       DECIMAL(18,0),
    PLSANIT_AM_MEAN    DECIMAL(18,0),
    CLOTHGDS_AM_MEAN   DECIMAL(18,0),
    AUTO_AM_MEAN       DECIMAL(18,0),

    PRIMARY KEY (SEX_CD, AGE)
);

